package com.hkllzh.android.util.datetime;

/**
 * 时间常量
 * <p/>
 * lizheng -- 2015/08/23
 * <p/>
 * BaseAndroid
 */
public class TimeConstant {

    /**
     * 一秒。以秒计算
     */
    public static final long ONE_SECOND = 1L;

    /**
     * 一分钟。以秒计算
     */
    public static final long ONE_MINUTE_SECOND = 60L; // 60

    /**
     * 一天。以秒计算
     */
    public static final long ONE_DAY_SECOND = 86400L; // 24 * 60 * 60

    /**
     * 一周。以秒计算
     */
    public static final long ONE_WEEK_SECOND = 604800L; // 7 * 24 * 60 * 60

    /**
     * 30天。以秒计算
     * 一般意义上的一个月
     */
    public static final long THIRTY_DAY_SECOND = 2592000L; // 30 * 24 * 60 * 60


    /**
     * 一秒。以毫秒计算
     */
    public static final long ONE_MILLISECONDS = 1_000L; // 1 * 1000

    /**
     * 一分钟。以毫秒计算
     */
    public static final long ONE_MINUTE_MILLISECONDS = 60_000L; // 60 * 1000

    /**
     * 一天。以毫秒计算
     */
    public static final long ONE_DAY_MILLISECONDS = 86400_000L; // 24 * 60 * 60 * 1000

    /**
     * 一周。以毫秒计算
     */
    public static final long ONE_WEEK_MILLISECONDS = 604800_000L; // 7 * 24 * 60 * 60 * 1000

    /**
     * 30天。以毫秒计算
     * 一般意义上的一个月
     */
    public static final long THIRTY_DAY_MILLISECONDS = 2592000_000L; // 30 * 24 * 60 * 60 * 1000



}
